package id.ac.ukdw.fti.rpl.Obtineo.modal;

public class People {
    private String personLookup;
    private String name;
    private String gender;

    public String getPersonLookup() {
        return personLookup;
    }
    public void setPersonLookup(String personLookup) {
        this.personLookup = personLookup;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getGender() {
        return gender;
    }
    public void setGender(String gender) {
        this.gender = gender;
    }

}